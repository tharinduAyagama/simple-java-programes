class mathsMethods{
    public static void main(String[] args) {
        System.out.println(Math.abs(-16.6));     //absolute value(mapankaya)
        System.out.println(Math.ceil(17.5));     //upper value
        System.out.println(Math.floor(16.5));    //lower value
        System.out.println(Math.max(18.5, 45.1));  //max value
        System.out.println(Math.min(18.5, 45.1));  //min value
        System.out.println(Math.pow(4, 3));   //qube of 4
        System.out.println(Math.sqrt(60));    //squar root
    }
}